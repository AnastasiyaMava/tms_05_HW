class Calculator:

    def __init__(self):
        pass

    def sum(self, x1, x2):
        return x1 + x2

    def diduction(self, x1, x2):
        return x1 - x2

    def multiplication(self, x1, x2):
        return x1 * x2

    def division(self, x1, x2):
        if x2 != 0:
            return x1 / x2
